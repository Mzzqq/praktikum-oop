public class Matematika {
    int a, b;
    public int pertambahan(int a, int b) {
        this.a = a;
        this.b = b;

        return a + b;
    }

    public int pengurangan(int a, int b) {
        this.a = a;
        this.b = b;

        return a - b;
    }

    public int perkalian(int a, int b) {
        this.a = a;
        this.b = b;

        return a * b;
    }

    public double pembagian(int a, int b) {
        this.a = a;
        this.b = b;

        return (double) a / b;
    }
}
